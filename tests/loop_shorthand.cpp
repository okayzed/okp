using namespace std;

#include <iostream>

int main() {

    for (auto i = 0; i < 10; i++) {
        cout << i << " "; }
    cout << endl;

    for (auto i = 1; i < 10; i += 2) {
        cout << i << " "; }
    cout << endl;

    for (auto i = 10; i >= 0; i--) {
        cout << i << " "; }
    cout << endl;

    for (auto i = 10; i >= 0; i += -2) {
        cout << i << " "; }
    cout << endl; };



