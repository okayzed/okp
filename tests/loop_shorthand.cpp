using namespace std;

#include <iostream>

int main() {

    for (auto i = 0; i < 10; i++) {
        std::cout << i << " "; }
    std::cout << std::endl;

    for (auto i = 1; i < 10; i += 2) {
        std::cout << i << " "; }
    std::cout << std::endl;

    for (auto i = 10; i >= 0; i--) {
        std::cout << i << " "; }
    std::cout << std::endl;

    for (auto i = 10; i >= 0; i += -2) {
        std::cout << i << " "; }
    std::cout << std::endl; };



